// types in JavaScript

// JavaScript is a loosely typed or dynamic language, which means you don't have to declare the
// type of a variable ahead of time. This means you can have the same variable as different types.

var foo = 42; //foo is now a Number
var foo = 'bar'; // foo is now a String
var foo = true; // foo is now a Boolean

